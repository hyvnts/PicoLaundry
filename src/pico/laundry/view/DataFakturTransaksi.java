    private void tableRender() {
        tabelData.setModel(fakturTableModel);
        tabelData.getColumnModel().getColumn(0).setPreferredWidth(80);
        tabelData.getColumnModel().getColumn(0).setPreferredWidth(100);
        tabelData.getColumnModel().getColumn(1).setPreferredWidth(150);
        tabelData.getColumnModel().getColumn(2).setPreferredWidth(100);
        tabelData.getColumnModel().getColumn(2).setPreferredWidth(100);

    }
    public Pemesanan getSelectionRow() {
        int index = tabelData.getSelectedRow();
        Pemesanan pemesanan = null;
        if (index >= 0) {
            pemesanan = fakturTableModel.getRows().get(tabelData.convertRowIndexToModel(index));
        }
        return pemesanan;
    }
